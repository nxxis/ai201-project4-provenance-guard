LOG_FILE = "audit_log.json"


# ---------------------------------------------------------------------------
# Audit log helpers
# ---------------------------------------------------------------------------

def load_log() -> list:
    """Load all entries from the JSON audit log. Returns empty list if missing."""
    if not os.path.exists(LOG_FILE):
        return []
    with open(LOG_FILE, "r") as f:
        try:
            return json.load(f)
        except json.JSONDecodeError:
            return []


def save_log(entries: list) -> None:
    """Persist the full list of entries back to disk."""
    with open(LOG_FILE, "w") as f:
        json.dump(entries, f, indent=2)


def append_log_entry(entry: dict) -> None:
    """Append a single structured entry to the audit log."""
    entries = load_log()
    entries.append(entry)
    save_log(entries)


