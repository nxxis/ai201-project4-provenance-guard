# Signal 1 — Groq LLM classification
# ---------------------------------------------------------------------------

LLM_PROMPT = """You are an AI content detection assistant. Your job is to assess whether a piece of text was written by a human or generated by an AI writing tool.

Analyze the text below and return ONLY a valid JSON object with a single field: "llm_score".

The score must be a float between 0.0 and 1.0:
- 1.0 means you are highly confident the text is AI-generated
- 0.0 means you are highly confident the text is human-written
- Use values between 0.3 and 0.7 for genuinely ambiguous cases

Signals of AI-generated text include: overly structured arguments, hedged phrasing (e.g. "it is important to note"), uniform sentence rhythm, generic vocabulary, and lack of personal voice.

Signals of human-written text include: irregular sentence lengths, personal anecdotes, colloquialisms, typos, strong or idiosyncratic opinions, and emotional register.

Respond with ONLY the JSON object. No explanation, no markdown, no extra text.

Example response: {{"llm_score": 0.82}}

Text to analyze:
\"\"\"
{text}
\"\"\""""


def get_llm_score(text: str) -> float:
    """
    Call Groq to get a semantic AI-likelihood score.

    Returns a float in [0.0, 1.0] where 1.0 = likely AI-generated.
    Falls back to 0.5 (uncertain) on any error.
    """
    try:
        response = groq_client.chat.completions.create(
            model="llama-3.3-70b-versatile",
            messages=[
                {
                    "role": "user",
                    "content": LLM_PROMPT.format(text=text),
                }
            ],
            temperature=0.0,   # deterministic output
            max_tokens=50,
        )

        raw = response.choices[0].message.content.strip()

        # Strip markdown fences if the model adds them despite instructions
        if raw.startswith("```"):
            raw = raw.split("```")[1]
            if raw.startswith("json"):
                raw = raw[4:]
            raw = raw.strip()

        parsed = json.loads(raw)
        score = float(parsed["llm_score"])
        return max(0.0, min(1.0, score))   # clamp to [0, 1]

    except Exception as e:
        app.logger.error(f"get_llm_score error: {e}")
        return 0.5   # fallback: uncertain


# ---------------------------------------------------------------------------
# Signal 2 — Stylometric heuristics
# ---------------------------------------------------------------------------

import re
import math


def get_stylometric_score(text: str) -> float:
    """
    Compute a structural AI-likelihood score from two stylometric metrics:
      1. Sentence length variance  — AI text is more uniform (low variance)
      2. Type-token ratio (TTR)    — AI text has moderate lexical diversity

    Both metrics are normalized so that more AI-like → closer to 1.0.
    Returns a float in [0.0, 1.0].
    """
    # --- Tokenize into sentences and words ---
    sentences = [s.strip() for s in re.split(r'[.!?]+', text) if s.strip()]
    words = re.findall(r'\b\w+\b', text.lower())

    # Need at least 2 sentences and 10 words for meaningful signal
    if len(sentences) < 2 or len(words) < 10:
        return 0.5  # too short to assess — return uncertain

    # --- Metric 1: Sentence length variance ---
    sent_lengths = [len(re.findall(r'\b\w+\b', s)) for s in sentences]
    mean_len = sum(sent_lengths) / len(sent_lengths)
    variance = sum((l - mean_len) ** 2 for l in sent_lengths) / len(sent_lengths)

    # High variance = human-like. Normalize: clamp variance to [0, 100],
    # then invert so low variance → score near 1.0 (AI-like).
    variance_clamped = min(variance, 100.0)
    variance_score = 1.0 - (variance_clamped / 100.0)

    # --- Metric 2: Type-token ratio (TTR) ---
    unique_words = len(set(words))
    ttr = unique_words / len(words)

    # Human writing tends toward higher TTR (more diverse vocabulary).
    # AI writing clusters around moderate TTR (~0.50–0.70 at typical lengths).
    # Normalize: TTR below 0.6 → more AI-like → score near 1.0.
    # TTR above 0.6 → more human-like → score near 0.0.
    ttr_score = max(0.0, min(1.0, (0.6 - ttr) / 0.6 + 0.5))
    ttr_score = max(0.0, min(1.0, ttr_score))

    # --- Combine into single stylometric score ---
    stylometric_score = (variance_score + ttr_score) / 2.0
    return round(max(0.0, min(1.0, stylometric_score)), 4)


# ---------------------------------------------------------------------------
# Confidence scoring
# ---------------------------------------------------------------------------

def compute_confidence(llm_score: float, stylometric_score: float) -> tuple:
    """
    Combine both signal scores into a single calibrated confidence value.

    Weights (from planning.md):
        LLM signal:          0.6  (semantic — stronger signal)
        Stylometric signal:  0.4  (structural — corroborating signal)

    Thresholds:
        >= 0.65  → likely_ai
        >  0.35  → uncertain
        <= 0.35  → likely_human

    Returns: (confidence: float, attribution: str)
    """
    confidence = round((0.6 * llm_score) + (0.4 * stylometric_score), 4)

    if confidence >= 0.65:
        attribution = "likely_ai"
    elif confidence > 0.35:
        attribution = "uncertain"
    else:
        attribution = "likely_human"

    return confidence, attribution


