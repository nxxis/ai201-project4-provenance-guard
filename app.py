@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    """
    Accept a text submission for attribution analysis.

    Request body (JSON):
        text        (str, required) — the content to classify
        creator_id  (str, required) — identifier for the submitting creator

    Returns (JSON):
        content_id         — unique ID for this submission (use for appeals)
        attribution        — "likely_ai" | "uncertain" | "likely_human"
        confidence         — float [0, 1] weighted combination of both signals
        label              — transparency label text shown to users
        llm_score          — raw Signal 1 output
        stylometric_score  — raw Signal 2 output
    """
    data = request.get_json(silent=True)

    # --- Input validation ---
    if not data:
        return jsonify({"error": "Request body must be JSON."}), 400
    if not data.get("text", "").strip():
        return jsonify({"error": "Field 'text' is required and cannot be empty."}), 400
    if not data.get("creator_id", "").strip():
        return jsonify({"error": "Field 'creator_id' is required."}), 400

    text = data["text"].strip()
    creator_id = data["creator_id"].strip()

    # --- Generate a unique content ID ---
    content_id = str(uuid.uuid4())
    timestamp = datetime.now(timezone.utc).isoformat()

    # --- Signal 1: Groq LLM ---
    llm_score = get_llm_score(text)

    # --- Signal 2: Stylometric heuristics ---
    stylometric_score = get_stylometric_score(text)

    # --- Confidence scoring: weighted combination ---
    confidence, attribution = compute_confidence(llm_score, stylometric_score)

    # --- Label (placeholder until Milestone 5) ---
    label = "[Label generation coming in Milestone 5]"

    # --- Write structured audit log entry ---
    log_entry = {
        "content_id": content_id,
        "creator_id": creator_id,
        "timestamp": timestamp,
        "attribution": attribution,
        "confidence": confidence,
        "llm_score": llm_score,
        "stylometric_score": stylometric_score,
        "status": "classified",
        "appeal_reasoning": None,
        "appeal_timestamp": None,
    }
    append_log_entry(log_entry)

    return jsonify({
        "content_id": content_id,
        "attribution": attribution,
        "confidence": confidence,
        "label": label,
        "llm_score": llm_score,
        "stylometric_score": stylometric_score,
    }), 200


@app.route("/log", methods=["GET"])
def get_log():
    """
    Return all audit log entries as JSON, most recent first.
    In production this endpoint would require authentication.
    """
    entries = load_log()
    entries_desc = list(reversed(entries))
    return jsonify({"count": len(entries_desc), "entries": entries_desc}), 200


